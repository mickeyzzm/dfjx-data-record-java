package com.datarecord.webapp.sys.origin.entity;

public class OriginNature {

    private Integer id;

    private String origin_nature_code;

    private String origin_nature_name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getOrigin_nature_code() {
        return origin_nature_code;
    }

    public void setOrigin_nature_code(String origin_nature_code) {
        this.origin_nature_code = origin_nature_code;
    }

    public String getOrigin_nature_name() {
        return origin_nature_name;
    }

    public void setOrigin_nature_name(String origin_nature_name) {
        this.origin_nature_name = origin_nature_name;
    }
}
